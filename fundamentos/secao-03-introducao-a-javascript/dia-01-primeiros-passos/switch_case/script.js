let grade = 'xablau';

switch (grade) {
    case 'aprovada':
        console.log('Parabéns! Você foi aprovado');
        break
    case 'lista':
        console.log('Você está na nossa lista de espera');
        break
    case 'reprovada':
        console.log('Você foi reprovado!');
        break
    default:
        console.log('Informação incorreta')
}